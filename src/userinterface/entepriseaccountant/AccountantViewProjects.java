    private void btnFundAllocationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFundAllocationActionPerformed
        
        int selectedRow = tblProjects.getSelectedRow();
        
        if(selectedRow >= 0){
            Project project = (Project)tblProjects.getValueAt(selectedRow, 0);
            FundAllocationJPanel fajp = new FundAllocationJPanel(userProcessContainer, enterprise, project);
            userProcessContainer.add("FundAllocationJPanel", fajp);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning",JOptionPane.WARNING_MESSAGE);
        }        
    }//GEN-LAST:event_btnFundAllocationActionPerformed

