    public void populateProjectTable(){
        DefaultTableModel dtm =  (DefaultTableModel) tblProjects.getModel() ;
        dtm.setRowCount(0);
        for(Project p: enterprise.getProjectDirectory().getProjectList()){
            Object row[] = new Object[4];
            row[0] = p;
            row[1] = p.getTotalFundsRequired();
            row[2] = p.getTotalFundsAllocated();
            row[3] = p.fundsRemaining();
            
            dtm.addRow(row);
        }
    }

