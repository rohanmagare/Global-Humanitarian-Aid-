    private void updateState(){
        if(projectView != null){
            txtEvent.enable(false);
            txtFundsReq.enable(false);
            txtProjectName.enable(false);
            txtVolunteer.enable(false);
            btnAddEvent.enable(false);
            txtEvent.setText(projectView.getEvent());
            txtFundsReq.setText(String.valueOf(projectView.fundsRemaining()));
            txtVolunteer.setText(String.valueOf(projectView.getVolunteersRemaining()));
        }
    }

